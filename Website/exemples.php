      <h2 id="ampoule">L'ampoule de Livermore</h2>
      <p class="lead">L’ampoule est l’un des premiers exemples de l’obsolescence programmée. Il est apparu suite au cartel Phoebus, où les 
        grands constructeurs d’ampoules, l’américain General Electric, l’allemand Osram, Philips et d’autres, se sont mis d’accord
         pour réduire la durée de vie des lampes à filament.
     </p>
    
      <div class=row>
        <div class="col-md-3">
              <img class="featurette-image img-responsive img-thumbnail" data-src="holder.js/500x500/auto" alt="Generic placeholder image" src="assets/livermore.jpg">
              <figcaption>L’ampoule de Livermore</figcaption>
        </div>
    
        <div class="col-md-9">
          <p class="lead">Avant le cartel, les industries produisaient des lampes avec une durée de vie allant  jusqu’à 2300 heures 
            en moyenne.  Ils ont ensuite décidé que la durée de vie des ampoules ne devrait pas dépasser 1000h. 
          </p>
          <p class="lead">
            D’après plusieurs chercheurs, le cartel avait pour but de limiter la durée de vie des lampes et  de pousser les gens à acheter plus 
            de lampes. Il y a aussi une étude qui a montré que le filament de fer des ampoules de nos jours est plus fragile que sur les 
            anciennes ampoules. 
           </p>
        </div>
      </div>
      
      <div class=row>
        <div class="col-md-9">
          <p class="lead">Pourtant, l’ampoule de Livermore est un exemple des lampes qui ont dépassé les 1000 heures. La vieille ampoule, 
            installée aux Etats-Unis en 1901 dans une caserne des pompiers, est allumée sans interruption depuis 114 ans. Mais pourquoi 
            fonctionne-t-elle toujours ? C’est grâce à sa faible luminosité et sa basse consommation. Cette ampoule était concue pour 
            consommer 60 watt, mais aujourd’hui, elle a une puissance de 4 watt. 
          </p>
          <p class="lead">
            Des études ont montré que l’ampoule de Livermore ne produit que 0,3 % de sa luminosité originale. Or moins la lampe est 
            lumineuse, plus sa durée de vie est importante.
          </p>
