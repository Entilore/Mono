La volonté de Henry Ford était d’avoir une voiture robuste mais peu chère. Etant de la vieille école, il considérait que ses produits devaient être durables. Il permit ainsi à de nombreuses familles de la classe moyenne de posséder une voiture, chose impensable jusque là. 



